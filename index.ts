// percentage program using if,else if and else
let percentage = "57.2"
if (percentage >= "90"){
    console.log("A-one Grade")
}
else if(percentage >="80"){
    console.log("A Grade")
}
else if(percentage >="70"){
    console.log("B Grade")
}
else if(percentage >="60"){
    console.log("C Grade")
}
else if(percentage >="50"){
    console.log("D Grade")
}
else{
    console.log("Fail")
}